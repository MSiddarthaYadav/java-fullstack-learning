public class loopstatement{
    public static void main(String[]args){
//while loop
   int i=1;
        while(i<=5){
            System.out.println(i);
            i++;

        }
        
//do while loop
        int j=6;
        do
        {
             System.out.println(j);
             j++;
        }while(j<5);

//for loop
        for(int k=1;k<=7;k++)
        {
            System.out.println(k);
        }
    }
}